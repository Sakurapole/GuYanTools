let failed = false;

for (const [relativePath, markers] of checks) {
  const fullPath = path.join(root, relativePath);
  if (!fs.existsSync(fullPath)) {
    console.error(`[quick-launch] missing file: ${relativePath}`);
    failed = true;
    continue;
  }

  const content = fs.readFileSync(fullPath, 'utf8');
  for (const marker of markers) {
    if (!content.includes(marker)) {
      console.error(`[quick-launch] ${relativePath} missing marker: ${marker}`);
      failed = true;
    }
  }
}

for (const [relativePath, forbiddenMarkers] of forbiddenChecks) {
  const fullPath = path.join(root, relativePath);
  if (!fs.existsSync(fullPath)) {
    continue;
  }

  const content = fs.readFileSync(fullPath, 'utf8');
  for (const marker of forbiddenMarkers) {
    if (content.includes(marker)) {
      console.error(`[quick-launch] ${relativePath} contains forbidden marker: ${marker}`);
      failed = true;
    }
  }
}

const providerDir = path.join(root, 'desktop/src/main/quick-launch/providers');
for (const fileName of [
  'internal_route_provider.ts',
  'terminal_provider.ts',
  'ssh_provider.ts',
  'ftp_provider.ts',
  'todo_provider.ts',
  'knowledge_provider.ts',
  'plugin_provider.ts',
  'app_provider.ts',
  'everything_file_provider.ts',
]) {
  if (!fs.existsSync(path.join(providerDir, fileName))) {
    console.error(`[quick-launch] missing provider: ${fileName}`);
    failed = true;
  }
}

const packageJson = JSON.parse(fs.readFileSync(path.join(root, 'desktop/package.json'), 'utf8'));
if (packageJson.scripts?.['verify:quick-launch'] !== 'node scripts/verify-quick-launch.cjs') {
  console.error('[quick-launch] desktop/package.json missing verify:quick-launch script');
  failed = true;
}

if (failed) {
  process.exit(1);
}

console.log('[quick-launch] implementation markers verified');
